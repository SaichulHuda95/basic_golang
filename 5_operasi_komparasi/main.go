// Contoh Operasi Komparasi (Perbandingan)
// Operator: <, >, <=, >=, ==, !=, is, is not
package main

import "fmt"

func main() {
	a := 10
	b := 20

	// Operasi Komparasi
	fmt.Println("a :", a)   // 10
	fmt.Println("b :", b)   // 20
	fmt.Println("a < b:", a < b)   // true
	fmt.Println("a > b:", a > b)   // false
	fmt.Println("a <= b:", a <= b) // true
	fmt.Println("a >= b:", a >= b) // false
	fmt.Println("a == b:", a == b) // false
	fmt.Println("a != b:", a != b) // true
}