// Contoh berbagai tipe data di Golang
package main

import "fmt"

func main ()  {
	//Tipe data Integer (bilangan bulat)
	data_int := 10
	//Tipe data Float (bilangan desimal)
	data_float := 3.14
	//Tipe data String (teks/kalimat)
	data_string := "Hello Golang"
	//Tipe data Boolean (True/False)
	data_bool := true

	//Menampilkan semua tipe data
	fmt.Println("Contoh Data Integer : ", data_int)
	fmt.Println("Contoh Data Float : ", data_float)
	fmt.Println("Contoh Data String : ", data_string)
	fmt.Println("Contoh Data Boolean : ", data_bool)
}